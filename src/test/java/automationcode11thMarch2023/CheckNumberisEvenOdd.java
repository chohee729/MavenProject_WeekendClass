package automationcode11thMarch2023;

public class CheckNumberisEvenOdd {

	public static void main(String[] args) {
		int a = 39; int b =20;
		
		if(a%2 == 0) {
			System.out.println("a is an even number:" + a);
		}else {
			System.out.println("a is not an even number");
		}
		if (b%2 == 0) {
			System.out.println("b is an even number" +b);
		}else {
			System.out.println("b is not an even number");
		}
	}

}
